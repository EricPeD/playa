export function normalizeLanguage(code?: string | null): SupportedLanguage {
  const value = (code ?? DEFAULT_LANGUAGE).toLowerCase();
  const found = SUPPORTED_LANGUAGES.find((option) => option.code === value);
  return found ? (found.code as SupportedLanguage) : DEFAULT_LANGUAGE;
}

export function getUiText(code: string | null | undefined, key: string): string {
  const lang = normalizeLanguage(code);
  return UI_TEXT[lang][key] ?? UI_TEXT[DEFAULT_LANGUAGE][key] ?? key;
}

export function getLanguageLabel(code?: string | null): string {
  const lang = normalizeLanguage(code);
  return SUPPORTED_LANGUAGES.find((option) => option.code === lang)?.label ?? 'Español';
}

export function getStoredLanguage(): SupportedLanguage | null {
  if (typeof window === 'undefined') return null;
  const value = window.localStorage.getItem('playa-language');
  if (!value) return null;
  return normalizeLanguage(value);
}

export function setStoredLanguage(code?: string | null): void {
  if (typeof window === 'undefined') return;
  const lang = normalizeLanguage(code);
  window.localStorage.setItem('playa-language', lang);
}